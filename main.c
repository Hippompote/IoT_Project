void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if (GPIO_Pin == GPIO_PIN_0){
		// Si on lit un 1 => on est sur un front montant
		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0)){
			LowState = __HAL_TIM_GET_COUNTER(&htim2)-HighState;
			TIM2->CNT = 0;
			cyclic_ratio = HighState/(HighState+LowState);
			integrateflag = 1;
		}
		// Si on lit 0 => on est sur un front descendant
		if (!HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0)){
			HighState = __HAL_TIM_GET_COUNTER(&htim2);
		}
	}
}

float areacalc(void){
	float current_error = fabsf(mean_cr - cyclic_ratio);

	area = area + current_error - crbuff[crptr];
	crbuff[crptr] = current_error;
	crptr++;

	if (crptr >= sizeof(crbuff)){
		crptr = 0;
	}

	integrateflag = 0;

	return area;
}

